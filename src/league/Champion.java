package league;

public class Champion {
    public String name;
    public int hp;
    public int ad;
    public int ap;
    public int arm;
    public int mr;

    public Champion(String name, int hp, int ad, int ap, int arm, int mr) {
        this.name = name;
        this.hp = hp;
        this.ad = ad;
        this.ap = ap;
        this.arm = arm;
        this.mr = mr;
    }
}
